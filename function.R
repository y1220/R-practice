# Create a function pow_two()
pow_two <- function(arg) {
  arg*arg
}


# Use the function
pow_two(12)

# Create a function sum_abs()
sum_abs <- function(arg1, arg2) {
  abs(arg1)+ abs(arg2)
}


# Use the function
sum_abs(-2, 3)

